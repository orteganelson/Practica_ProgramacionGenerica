    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cedula = jTextField1.getText();
        String nombre = jTextField2.getText();
        String apellido = jTextField3.getText();
        String direccion = jTextField4.getText();
        
        if(cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty()){
            JOptionPane.showMessageDialog(this, "Complete los campos");
        }else{
            ctrAgendaPersona.create(new Persona(cedula, nombre, apellido, direccion));
            JOptionPane.showMessageDialog(this, "Registro completo");
            limpiar();
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

