    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cedula = jTextField1.getText();
        
        Optional<Persona> persona = ctrAgendaPersona.read(p -> cedula.equals(p.getCedula()));
        
        if(persona.isPresent()){
        
            Persona pers = persona.get();            
            jTextField2.setText(pers.getNombre());
            jTextField3.setText(pers.getApellido());
            jTextField4.setText(pers.getCedula());
            jTextField5.setText(pers.getDireccion());
            limpiarTabla();
            llenarTabla();
            activarParcialBotones();
        }else{
            JOptionPane.showMessageDialog(this, "Cedula no encontrada");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

