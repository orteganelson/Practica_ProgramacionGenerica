    public void limpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
        }
    }

    public void llenarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        codigo = jTextField4.getText();
        
        List<Telefono> listaTelefono = ctrAgendaTelefono.findAll();
        List<Telefono> lista= new ArrayList<>();
        
        for (Telefono telefono : listaTelefono) {
            if(codigo.equals(telefono.getCodigo()))
                lista.add(telefono);
        }
        
        if(lista.isEmpty()){
            JOptionPane.showMessageDialog(this, "Este usuario no presenta teléfonos");
        }else{
            for (Telefono telefono : lista) {
                Object[] objeto = {telefono.getNumero(), 
                    telefono.getTipo(), telefono.getOperadora()};
                modelo.addRow(objeto);
            }
        }
    }
    
